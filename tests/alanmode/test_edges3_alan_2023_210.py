@pytest.mark.parametrize("load", list(am.LOADMAP.values()))
def test_spectrum_averaging(
    alanmode_averaged_spectra: dict[str, GSData],
    legacy_averaged_spectra: dict[str, GSData],
    load: str,
):
    if not RAW_SPECTRUM_DIR.exists():
        pytest.skip("Raw spectrum data not available, cannot test spectrum averaging.")

    ours = alanmode_averaged_spectra[load]

    np.testing.assert_allclose(legacy_averaged_spectra[load].freqs, ours.freqs)
    np.testing.assert_allclose(
        legacy_averaged_spectra[load].data[20:-20], ours.data[20:-20], atol=1e-6
    )


@pytest.mark.parametrize("legacy_key", [*list(am.LOADMAP.keys()), "lna"])
def test_unmodelled_s11(alanmode_raw_s11s, legacy_key):
    amode_key = "receiver_s11" if legacy_key == "lna" else am.LOADMAP[legacy_key]

    alanfreq, alans11 = am.read_s11_csv(LEGACY_DATA_PATH / f"s11{legacy_key}.csv")
    ourfreq, ours11 = alanmode_raw_s11s
    ours11 = ours11[amode_key]

    mask = (alanfreq >= ourfreq.min()) & (alanfreq <= ourfreq.max())
    np.testing.assert_allclose(alanfreq[mask], ourfreq)
    np.testing.assert_allclose(alans11[mask].real, ours11.real, atol=1e-10)
    np.testing.assert_allclose(alans11[mask].imag, ours11.imag, atol=1e-10)


@pytest.fixture(scope="module")
def legacy_modelled_s11s() -> QTable:
    return am.read_modelled_s11s(LEGACY_DATA_PATH / "s11_modelled.txt")


def test_modelled_s11(
    alanmode_modelled_s11s: QTable,
    legacy_modelled_s11s: QTable,
):
    alanfreq = legacy_modelled_s11s["freqs"]
    ourfreq = alanmode_modelled_s11s["freqs"]

    # Even though the legacy code ends up using modelled S11s only in the range
    # WFSTART to WFSTOP, the modelled S11s are actually generated across the full range
    # FSTART-FSTOP and written that way, so we need to mask them to compare
    mask = (alanfreq >= ourfreq.min()) & (alanfreq <= ourfreq.max())

    for key in alanmode_modelled_s11s.colnames:
        print("Testing modelled S11 for", key)

        # Test that all values are good to 1e-5
        np.testing.assert_allclose(
            alanmode_modelled_s11s[key],
            legacy_modelled_s11s[key][mask],
            atol=2e-5,
            rtol=0,
        )

        # Value not near the edges should be even better, at 1e-6
        np.testing.assert_allclose(
            alanmode_modelled_s11s[key][50:-50],
            legacy_modelled_s11s[key][mask][50:-50],
            atol=2e-6,
            rtol=0,
        )


def test_specal(alanmode_specal: Calibrator, legacy_specal: Calibrator):
    np.testing.assert_allclose(legacy_specal.freqs, alanmode_specal.freqs)
    np.testing.assert_allclose(legacy_specal.Tsca, alanmode_specal.Tsca, rtol=3e-6)
    np.testing.assert_allclose(legacy_specal.Toff, alanmode_specal.Toff, atol=4e-5)
    np.testing.assert_allclose(legacy_specal.Tunc, alanmode_specal.Tunc, atol=1.1e-6)
    np.testing.assert_allclose(legacy_specal.Tcos, alanmode_specal.Tcos, atol=2e-5)
    np.testing.assert_allclose(legacy_specal.Tsin, alanmode_specal.Tsin, atol=2.2e-5)


@pytest.mark.parametrize("key", am.LOADMAP.values())
def test_calibrated_temps(
    alanmode_calibrated_temps: dict[str, np.ndarray],
    legacy_calibrated_temps: dict[str, np.ndarray],
    key: str,
):
    alan = legacy_calibrated_temps[key]
    ours = alanmode_calibrated_temps[key]

    np.testing.assert_allclose(ours["freq"], alan["freq"], atol=0, rtol=0)
    np.testing.assert_allclose(ours["uncal"], alan["uncal"], atol=1e-6, rtol=0)
    np.testing.assert_allclose(ours["cal"], alan["cal"], atol=5e-5, rtol=0)
