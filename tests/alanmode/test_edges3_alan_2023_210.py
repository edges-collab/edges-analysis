@pytest.fixture(scope="module")
def alanmode_averaged_spectra(
    output_dir: Path,
    legacy_averaged_spectrum_paths: dict[str, Path],
) -> dict[str, GSData]:

    avg_spectrum_files = {
        amode_key: output_dir / f"sp{legacy_key}.txt"
        for legacy_key, amode_key in am.LOADMAP.items()
    }

    if not RAW_SPECTRUM_DIR.exists():
        # We're not on enterprise where the full data products are kept, so we cannot
        # run the full spectrum averaging (data is too big to store in repo)
        # Therefore, we short-circuit and use the legacy averaged spectra directly.
        for k, pth in avg_spectrum_files.items():
            legacy_file = legacy_averaged_spectrum_paths[k]
            pth.symlink_to(legacy_file)

    else:
        # We're on enterprise, so let's also test running the spectrum averaging.
        specfiles = DEFPARAMS.get_spectrum_files()
        for load, files in specfiles.items():
            outfile = avg_spectrum_files[load]
            gsd = am.acqplot7amoon(files, params=ACQPARAMS)
            am.write_spec_txt_gsd(gsd, outfile)

    return {
        k: am.read_spec_txt(pth, telescope="edges3", name=k)
        for k, pth in avg_spectrum_files.items()
    }


