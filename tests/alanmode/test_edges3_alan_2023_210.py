"""
Integration test for day 2023_210 comparing C-code outputs to alanmode functions.

Some tests in this module only run when on enterprise, where the raw spectra
are available. When not on enterprise, we short-circuit those tests by symlinking to the
legacy averaged spectra.

Legacy C code outputs are in `tests/data/alanmode/edges3-2023-210-alan/`.
S11 raw inputs are in `tests/data/alanmode/edges3-2023-210-raw/` (spectra aren't kept
there because they are too big).

Legacy C code outputs are generated by the the code in
https://github.com/edges-collab/alans-pipeline. Specifically, from top-level, run:

```bash
make
cd scripts/edges3
./docal_edges3_2023_210
cd ../..
cp results/edges3-2023-210/* <this_repo>/tests/data/alanmode/edges3-2023-210-alan/
```

These tests are very similar to the 2022_316 tests, but have some differences
(beyond just the datafiles used):

    - The assumed temperatures are different
    - The fstart/fstop are different than wfstart/wfstop in the calibration function
      (in 2022_316 the fstart/fstop is only specified on the acqplot7amoon step, while
      the fstart/fstop in edges() is equivalent to wfstart/wfstop).

Also, this test module gives us the chance to test the actual set of observations being
used for EDGES-3 first release.
"""

